public class while_dongusu {
    public static void main(String[] args)
     {
      int i= 0;

      // ekrana 10 tane yazı yazdırma işlemi.
      // eğer "true" değeri olur ise döngünün içerisine girer/devam eder.
      while(i < 10){
        System.out.println("Emirhan");
        i++; // sonsuz olmaması için i'yi artırmalıyız.
      }
      System.out.println("Döngü bitti.");
     }
}
